package Model;

import java.awt.Image;

public abstract class LaneObject {
	
	int xLoc;
	int yLoc;
	Image image;
		
	public Image getImage() {
		return image;
	}
	
	public int getY(){
		return yLoc;
	}
	
	public void setY(int i){
		yLoc+=i;
	}
	
	public int getX(){
		return xLoc;
	}

}
